  // ============================================================
  // 📊 البحث عن إيموجي
  // ============================================================
  static List<Map<String, String>> searchEmojis(String query) {
    if (query.isEmpty) return [];
    
    final lowerQuery = query.toLowerCase();
    return getAllEmojis().where((emoji) {
      return emoji['name']!.toLowerCase().contains(lowerQuery) ||
             emoji['code']!.contains(query);
    }).toList();
  }

  // ============================================================
  // 🎯 الحصول على إيموجي عشوائي
  // ============================================================
  static Map<String, String> getRandomEmoji() {
    final all = getAllEmojis();
    final randomIndex = DateTime.now().millisecondsSinceEpoch % all.length;
    return all[randomIndex];
  }
